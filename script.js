function startingblock () {
    var music = document.getElementById("ost");
    music.volume = 0.2;
    music.play()
    document.getElementById('nstart').id="nupleft"
    document.getElementById('buttonstart').id="baccueil"
    document.getElementById('menu').id="menu1"
    document.getElementById('baccueil').onclick="none"
}
